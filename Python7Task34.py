phrases = poem_string.split()
print(phrases)
 
vowels_number = [sum(x in 'аяуюыиоёэе' for x in a_phrase) for a_phrase in phrases]
print(vowels_number)

# Вариант с циклом
def elem_compare(vowels_number):
    elem = vowels_number[0]
    chk = True 
    for item in vowels_number:
        if elem != item:
            chk = False
            break
    if (chk == True):
        return "Есть ритм! Парам пам-пам :-)"
    else: 
        return "Нет ритма!!! Пам парам "
    
# Вариант без цикла через множество!
def elem_set(vowels_number):
    if len(set(vowels_number)) == 1 : #функция len() возвращает количество символов в строке 
        return "Есть ритм! Парам пам-пам :-)"
    else: 
        return "Нет ритма!!! Пам парам "
 
